def draw_number(board, x, y, n):    
    g = str(n)
    g = g[-4:]
    while len(g)<4:
        g = "*"+g
    print(">>> DRAWING",g)
    for i in g:
        if i=='*':
            j = 10
        else:
            j = ord(i)-ord('0')      
        board.draw_rect(x,y,4,5,numbers[10],trans=255)   
        board.draw_rect(x,y,4,5,numbers[j],palette_offset=2)
        x+=4
        